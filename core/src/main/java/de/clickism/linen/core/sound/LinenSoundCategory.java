package de.clickism.linen.core.sound;

/**
 * Enumeration of sound categories used in Linen.
 */
public enum LinenSoundCategory {
    /**
     * Master sound category.
     */
    MASTER,
    /**
     * Music sound category.
     */
    MUSIC,
    /**
     * Records sound category.
     */
    RECORDS,
    /**
     * Weather sound category.
     */
    WEATHER,
    /**
     * Block-related sounds.
     */
    BLOCKS,
    /**
     * Hostile creature sounds.
     */
    HOSTILE,
    /**
     * Neutral creature sounds.
     */
    NEUTRAL,
    /**
     * Player-related sounds.
     */
    PLAYERS,
    /**
     * Ambient sounds.
     */
    AMBIENT,
    /**
     * Voice sounds.
     */
    VOICE,
}
